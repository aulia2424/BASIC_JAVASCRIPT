// increment
console.log("====increment====");
var a = 0;
var b = 10;
var a = b++;
console.log(a);
console.log(b);

// pre increment
console.log("====pre increment====");
var c = 0;
var d = 10;
var c = ++d;
console.log(c);
console.log(d);

// decrement
console.log("====decrement====");
var e = 10;
var f = 8;
var e = f--;
console.log(e);
console.log(f);

// pre decrement
console.log("====pre decrement====");
var g = 8;
var h = 8;
var g = --h;
console.log(g);
console.log(h);
